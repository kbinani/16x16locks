export interface Random {
  gen(): number;
}

export const kDefaultRandom: Random = {
  gen: () => {
    return Math.random();
  },
};

function choose<T>(array: T[], random: Random): T {
  const i = Math.floor(random.gen() * array.length);
  return array[i];
}

export function generate(random: Random): Character {
  const frogHead = random.gen() < kFrogChance;
  const frogBody = random.gen() < kFrogChance;
  const head = frogHead ? kHeadFrog : kHead;
  const eye = frogHead ? 0 : choose(kEyeColors, random);
  const body = frogBody ? kBodyFrog : choose(kBodies, random);
  const hair = frogHead ? empty() : choose(kHairs, random);
  return {
    head,
    eye,
    body,
    hair,
  };
}

function overlay(out: Bitmap, pixels: Pixels, fixed: number = 0) {
  for (let y = 0; y < 16; y++) {
    for (let x = 0; x < 16; x++) {
      const idx = pixels.colors[y][x];
      const rgb = pixels.palette[idx];
      if (rgb !== 0) {
        if (fixed === 0) {
          out.set(x, y, rgb);
        } else {
          out.set(x, y, fixed);
        }
      }
    }
  }
}

export function image(character: Character): Bitmap {
  const out = new Bitmap();
  overlay(out, character.head);
  if (character.eye > 0) {
    overlay(out, kEyeSocket, character.eye);
  }
  overlay(out, character.body);
  overlay(out, character.hair);
  return out;
}
